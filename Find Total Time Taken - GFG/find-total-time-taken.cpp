class Solution {
  public:
    int totalTime(int n, vector<int> &arr, vector<int> &time) {
        map<int,int>m;
        for(int i=0;i<n;i++)
        m[arr[i]]+=1;
        
        int ti=0;
        for(auto x:m){
            ti+= 1+time[x.first -1]*(x.second - 1);
        }
        return ti-1;
    }
};


